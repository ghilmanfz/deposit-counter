         ? (int)$db->escape($_POST['product-client'])
         : 0;
       $client_value = $client_id > 0 ? "'".$db->escape($client_id)."'" : "NULL";
       if (is_null($_POST['product-photo']) || $_POST['product-photo'] === "") {
         $media_id = '0';
       } else {
         $media_id = remove_junk($db->escape($_POST['product-photo']));
       }
